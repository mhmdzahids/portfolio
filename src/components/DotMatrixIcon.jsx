export default function DotMatrixIcon({ name, size = '18px', className = '' }) {
  const matrix = ICONS[name] || ICONS.folder;

  return (
    <div
      className={`dot-icon-grid ${className}`}
      style={{ '--gs': size }}
      role="img"
      aria-label={`Dot matrix ${name} icon`}
    >
      {matrix.map((row, rIdx) =>
        row.split('').map((cell, cIdx) => (
          <div
            key={`${rIdx}-${cIdx}`}
            className={`dot-icon-cell ${cell === '1' ? 'filled' : ''}`}
          />
        ))
      )}
    </div>
  );
}
